print(10 + 3) # Addition
print(10 - 3) # Subtraction
print(10 * 3) # Multiplication
print(10 / 3) # Division 
print(10 // 3) # Division but rounds quotient to the nearest whole number
print(10 % 3) # Remainder
print(10 ** 3) # Exponents

x = 10
x += 3 # Shortcut to perform an operation an existing arithmetic variable (Augmented Assignment Operated)
print(x)