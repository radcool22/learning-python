course = 'Python for Beginners'
print(len(course)) # Prints the length of the string
print(course.upper()) # Makes all the characters uppercase
print(course.lower()) # Makes all the characters lowercase
print(course.title()) # Makes the first letter of each word capital and leaves rest in lowercase
print(course.find('Beginners')) # Finds the character position
print(course.replace('Beginners', 'Absolute Beginners')) # Replaces the text in a string
print('Python' in course) # Returns boolean value to check existence of something in a variable